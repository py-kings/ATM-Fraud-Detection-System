-- Triggers here
DELIMITER $$

CREATE TRIGGER trg_fraud

AFTER INSERT ON TRANSACTIONS

FOR EACH ROW

BEGIN

    IF NEW.TRANSACTION_AMOUNT >= 50000 THEN

        INSERT INTO FRAUD_ALERT
        (TRANSACTION_ID, RISK_LEVEL)

        VALUES
        (
            NEW.TRANSACTION_ID,
            GET_RISK_LEVEL(NEW.TRANSACTION_AMOUNT)
        );

    END IF;

END $$

DELIMITER ;
